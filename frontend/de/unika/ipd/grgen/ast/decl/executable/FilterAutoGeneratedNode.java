package de.unika.ipd.grgen.ast.decl.executable;

import java.util.ArrayList;
import java.util.Collection;
import java.util.Vector;

import de.unika.ipd.grgen.ast.BaseNode;
import de.unika.ipd.grgen.ast.FilterCharacter;
import de.unika.ipd.grgen.ast.IdentNode;
import de.unika.ipd.grgen.ast.decl.DeclNode;
import de.unika.ipd.grgen.ast.decl.pattern.IteratedNode;
import de.unika.ipd.grgen.ast.type.TypeNode;
import de.unika.ipd.grgen.ast.util.DeclarationPairResolver;
import de.unika.ipd.grgen.ast.util.Pair;
import de.unika.ipd.grgen.ir.IR;
import de.unika.ipd.grgen.ir.executable.FilterAutoGenerated;
import de.unika.ipd.grgen.ir.executable.Rule;

/**
 * AST node class representing auto-generated filters
 */
public class FilterAutoGeneratedNode extends FilterAutoNode implements FilterCharacter
{
	static {
		setName(FilterAutoGeneratedNode.class, "auto generated filter");
	}

	IdentNode ident;

	protected String name;
	protected ArrayList<String> entities; // null for auto, otherwise list with at least one element
	protected ArrayList<TypeNode> entityTypes;

	protected IdentNode actionUnresolved;
	protected TestDeclNode action;
	protected IteratedNode iterated;

	public FilterAutoGeneratedNode(IdentNode ident, String name, ArrayList<String> entities, IdentNode action)
	{
		super(ident);
		this.ident = ident;
		this.name = name;
		this.entities = entities;
		this.actionUnresolved = action;
	}

	/** returns children of this node */
	@Override
	public Collection<BaseNode> getChildren()
	{
		Vector<BaseNode> children = new Vector<BaseNode>();
		children.add(getValidVersion(actionUnresolved, action, iterated));
		return children;
	}

	/** returns names of the children, same order as in getChildren */
	@Override
	public Collection<String> getChildrenNames()
	{
		Vector<String> childrenNames = new Vector<String>();
		childrenNames.add("actionOrIterated");
		return childrenNames;
	}

	private static final DeclarationPairResolver<TestDeclNode, IteratedNode> actionOrIteratedResolver =
			new DeclarationPairResolver<TestDeclNode, IteratedNode>(TestDeclNode.class, IteratedNode.class);

	/** @see de.unika.ipd.grgen.ast.BaseNode#resolveLocal() */
	@Override
	protected boolean resolveLocal()
	{
		Pair<TestDeclNode, IteratedNode> actionOrIterated = actionOrIteratedResolver.resolve(actionUnresolved, this);
		if(actionOrIterated == null)
			return false;
		action = actionOrIterated.fst;
		iterated = actionOrIterated.snd;
		return action != null || iterated != null;
	}

	/** @see de.unika.ipd.grgen.ast.BaseNode#resolveLocal() */
	@Override
	protected boolean checkLocal()
	{
		if(ident == null) {
			actionUnresolved.reportError("Filters are only supported on named iterateds");
			return false;
		}

		if(entities == null) {
			if(!name.equals("auto")) {
				reportError("Unknown auto-generated filter " + name);
				return false;
			}
			if(iterated != null) {
				reportError("The auto filter is only available for test and rule.");
				return false;
			}
			return true;
		}
		if(!name.equals("orderAscendingBy")
			&& !name.equals("orderDescendingBy")
			&& !name.equals("groupBy")
			&& !name.equals("keepSameAsFirst")
			&& !name.equals("keepSameAsLast")
			&& !name.equals("keepOneForEach")
			&& !name.equals("keepOneForEachAccumulateBy")) {
			reportError("Unknown auto-generated filter " + name + " for def-variable(s) " + String.join(",", entities));
			return false;
		}
		if(entities != null) {
			entityTypes = new ArrayList<TypeNode>();
			for(int i = 0; i < entities.size(); ++i) {
				if(name.equals("keepOneForEachAccumulateBy") && i == entities.size() - 1)
					continue;

				String entity = entities.get(i);
				DeclNode resolvedEntity = action != null
						? action.pattern.tryGetMember(entity)
						: iterated.pattern.tryGetMember(entity);
				if(resolvedEntity == null) {
					reportError("Unknown entity " + entity + " in "
							+ (action != null ? action.getIdentNode() : iterated.getIdentNode()));
					return false;
				}
				entityTypes.add(resolvedEntity.getDeclType());
			}
		}
		return true;
	}

	public String getFilterName()
	{
		return name;
	}

	public String getFilterNameWithEntitySuffix()
	{
		StringBuilder sb = new StringBuilder();
		sb.append(name);
		if(entities != null && !entities.isEmpty()) {
			sb.append("<");
			boolean first = true;
			for(String entity : entities) {
				if(first)
					first = false;
				else
					sb.append(",");
				sb.append(entity);
			}
			sb.append(">");
		}
		return sb.toString();
	}

	public TestDeclNode getActionNode()
	{
		return action;
	}

	public IteratedNode getIteratedNode()
	{
		return iterated;
	}

	/** Returns the IR object for this autogen filter node. */
	public FilterAutoGenerated getFilterAutoGenerated()
	{
		return checkIR(FilterAutoGenerated.class);
	}

	@Override
	protected IR constructIR()
	{
		// return if the IR object was already constructed
		// that may happen in recursive calls
		if(isIRAlreadySet()) {
			return getIR();
		}

		FilterAutoGenerated filterAutoGen = new FilterAutoGenerated(name, entities);

		// mark this node as already visited
		setIR(filterAutoGen);

		Rule actionOrIterated = action != null ? action.getAction() : iterated.getAction();
		filterAutoGen.setAction(actionOrIterated);
		actionOrIterated.addFilter(filterAutoGen);

		if(entityTypes != null) {
			for(TypeNode entityType : entityTypes) {
				filterAutoGen.addEntityType(entityType.getType());
			}
		}

		return filterAutoGen;
	}

	public static String getUseStr()
	{
		return "auto generated filter";
	}
}
